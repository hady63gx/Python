# 5.迴圈敘述的練習-interest
# 某人A以10%單利投資100000元，某人B則以5%複利投資100000元。計算多少年後某人B的投資可以超過某人A，並將此時兩人錢數印出。(27年後)

# 提示：單利公式：P(1+r*n)    複利公式：P(1+r)^n
# P：本金，r：利率，n：多少年
# (備註︰(1+r)^n 表示 (1+r) 的 n 次方)
y = 1
Start_count = 1
while Start_count == 1:
    a = 100000 * (1 + 0.1 * y)
    b = 100000 * (1 + 0.05) ** y
    if b > a:
        print("第", y, "年:某A有", a, "元\t", "某B有", b, "元")
        Start_count = 0
    else:
        y += 1
